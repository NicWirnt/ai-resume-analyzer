import Navbar from "~/components/Navbar";
import {type FormEvent, useState} from "react";
import FileUploader from "~/components/FileUploader";
import {usePuterStore} from "~/lib/puter";
import {useNavigate} from "react-router";
import {convertPdfToImage} from "~/lib/pdfToImage";
import {generateUUID} from "~/utils/format";
import {prepareInstructions} from "../../constants";

const upload = () =>{
    const { auth, isLoading, fs, ai, kv } = usePuterStore();
    const navigate = useNavigate();
    const [isProcessing, setIsProcessing] = useState(false);
    const [statusText, setStatusText] = useState('');
    const [file, setFile] = useState<File | null>(null)

    const handleFileSelect = (file:File | null) => {
        setFile(file);
    }

    const handleAnalyze = async ({
        companyName,
        jobTitle,
        jobDescription,
        file
       }:{
        companyName:string,
        jobTitle:string,
        jobDescription:string,
        file:File,
    }) => {
        setIsProcessing(true);
        setStatusText('Uploading file...');
        const uploadedFileResponse = await fs.upload([file]);
        const uploadedFile = Array.isArray(uploadedFileResponse) ? uploadedFileResponse[0] : uploadedFileResponse;

        if(!uploadedFile) return setStatusText('Error uploading file');

        setStatusText('Converting to image ...')
  
        const imageFile = await convertPdfToImage(file);
        if(!imageFile.file) {
            console.error('PDF conversion error:', imageFile.error);
            return setStatusText('Error converting to image');
        }

        setStatusText('Uploading image...')
        const uploadedImageResponse = await fs.upload([imageFile.file]);
        const uploadedImage = Array.isArray(uploadedImageResponse) ? uploadedImageResponse[0] : uploadedImageResponse;
        
        if(!uploadedImage) return setStatusText('Error uploading image');
        setStatusText(('Preparing data...'))

        const uuid = generateUUID();
        const data = {
            id:uuid,
            resumePath:uploadedFile.path,
            imagePath:uploadedImage.path,
            companyName,
            jobTitle,
            jobDescription,
            feedback:'',
        }
        await kv.set(`resume:${uuid}`, JSON.stringify(data));
        setStatusText('Analyzing resume...');

        const feedback = await ai.feedback(
            uploadedFile.path,
            prepareInstructions({
                jobTitle,
                jobDescription
            })
        )

        if(!feedback) return setStatusText('Error: Failed to analyze resume');
        
        const feedbackText = typeof feedback.message.content === 'string' ? feedback.message.content : feedback.message.content[0].text
        console.log(feedbackText)
        
        // Strip markdown code blocks if present
        const jsonMatch = feedbackText.match(/```(?:json)?\s*([\s\S]*?)\s*```/) || [null, feedbackText];
        const cleanedFeedback = jsonMatch[1]?.trim() || feedbackText.trim();
        
        try {
            data.feedback = JSON.parse(cleanedFeedback)
        } catch (e) {
            console.error("Failed to parse AI feedback:", e);
            console.log("Original text:", feedbackText);
            console.log("Cleaned text:", cleanedFeedback);
            return setStatusText('Error: Failed to parse analysis results');
        }

        await kv.set(`resume:${uuid}`, JSON.stringify(data));
        setStatusText('Resume analyzed successfully!');
        console.log(data);
    }

    const handleSubmit = (e:FormEvent<HTMLFormElement>) => {
        e.preventDefault();
        const form = e.currentTarget.closest('form');
        if (!form) return;
        const formData = new FormData(form);

        const companyName = formData.get('company-name') as string;
        const jobTitle = formData.get('job-title') as string;
        const jobDescription = formData.get('job-description') as string;

        if(!file) return;

        handleAnalyze({companyName, jobTitle, jobDescription, file})

    }

    return (
        <main className="bg-[url('images/bg-main.svg')] bg-cover">
            <Navbar />
            <section className="main-section">
                <div className={'page-heading py-16'}>
                    <h1>Smart feedback for your dream job!</h1>
                    {isProcessing ? (
                        <>
                        <h2>{statusText}</h2>
                            <img src={'/images/resume-scan.gif'} className={'w-full'} alt={'resumeScan'}/>
                        </>
                    ) : (
                        <h2>Drop your resume for an ATS score and improvement tips</h2>
                    )}
                    {!isProcessing && (
                        <form id={'upload-form'} onSubmit={handleSubmit}
                        className={'flex flex-col gap-4 mt-8'}
                        >
                            <div className={'form-div'}>
                                <label htmlFor={'company-name'}>Company Name</label>
                                <input type={'text'} id={'company-name'} name={'company-name'} placeholder={'Company Name'} />
                            </div>
                            <div className={'form-div'}>
                                <label htmlFor={'job-title'}>Job Title</label>
                                <input type={'text'} id={'job-title'} name={'job-title'} placeholder={'Job Title'} />
                            </div>
                            <div className={'form-div'}>
                                <label htmlFor={'job-description'}>Company Name</label>
                                <textarea rows={5} id={'job-description'} name={'job-description'} placeholder={'Job Description'} />
                            </div>
                            <div className={'form-div'}>
                                <label htmlFor={'uploader'}>Upload Resume</label>
                                <FileUploader file={file} onFileSelect={handleFileSelect}/>
                            </div>
                            <button className={'primary-button'} type={'submit'}>Analyze Resume</button>
                        </form>
                    )}
                </div>
            </section>
        </main>
    )
}

export default upload;